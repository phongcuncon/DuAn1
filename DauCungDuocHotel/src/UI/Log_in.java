    void dangnhap() {
        String tendn = txtTenDN.getText();
        String matkhau = new String(psMk.getPassword());
        NhanVien nhanvien =   dao.findNhanVien(tendn);
        if (nhanvien == null) {
            MsgBox.alert(this, "Sai tên đăng nhập !");
        } else if (!matkhau.equals(nhanvien.getMatKhau())) {
            MsgBox.alert(this, "Sai mật khẩu !");
            c++;
            if(c > 3){
                if(MsgBox.confirm(this, "Bạn có muốn reset lại mật khẩu không?")){
                    new QuenMatKhauJDialog(this, rootPaneCheckingEnabled).setVisible(true);
                }else{
                }
            }
        } else {
            Auth.user = nhanvien; // user = tendn
            this.dispose();
        }
    }

    void exit() {
        if (MsgBox.confirm(this, "Bạn muốn kết thúc chứ ?")) {
            System.exit(0);
        }
    }
